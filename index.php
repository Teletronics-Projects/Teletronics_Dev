			<div class="col-lg-12">
	 				<h4 class="heading4"><u> What Is a WSP?</u></h4>
				<div class="col-lg-12 whatwedo">
					<p> <span style="font-size: 20px; color: #f78f1d ">Workplace Skills Plan</span> outlines how an organisation/ employer is going to address the training and development needs within the workplace. It assists employers in identifying and providing relevant training that will address the skills gaps within the organisation. Compiling a WSP ensures that training is not only reactive to needs that emerge but also speaks to the overall organisational strategy, as well as encourages a holistic and sustainable approach to skills development. A well-thought-out WSP will ensure that the skills that an organisation lacks are addressed. This, in turn, will result in decreased training and development costs as development efforts are more focused.</p>
				</div>
			</div>

			<div class="col-lg-12">
					<h4 class="heading4"><u> Why Is a WSP Important?</u></h4>
				<div class="col-lg-12 whatwedo">
					<p>The Workplace Skills Plan (WSP) - documents the skills needs of a company and describes the range of skills development interventions that the organisation will use to address these needs.
						A facilitator must develop and submit a WSP every year in order to comply with South Africa’s current Skills Development legislation. A registered Skills Development Facilitator (SDF) or another qualified person will compile the WSP and submit it to the Sector Education and Training Authority (SETA).
						By complying with legislative requirements, a company is granted access to the various SETA grants available for skills training. However, in order to qualify for these grants, organisations must also ensure that their Annual Training Reports (ATR)</p>
